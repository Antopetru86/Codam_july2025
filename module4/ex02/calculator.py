#!/usr/bin/env python3

num1 = input("Give me the first number: ")
num2 = input("Give me the second number: ")

num1_int = int(num1)
num2_int = int(num2)
print("Thank you!")

add_num = num1_int + num2_int
sub_num = num1_int - num2_int
div_num = num1_int / num2_int
mul_num = num1_int * num2_int


print(f"{num1} + {num2} = {add_num}")
print(f"{num1} - {num2} = {sub_num}")
print(f"{num1} / {num2} = {div_num}")
print(f"{num1} * {num2} = {mul_num}")


     
