#!/usr/bin/env python3
age_input= input("Please tell me your age: ")
user_age = int(age_input)
new_age10= user_age+10
new_age20= user_age+20
new_age30= user_age+30

print(f"You are currently {user_age} years old.\n"
      f"In 10 years you will be {new_age10} years old.\n"
      f"In 20 years you will be {new_age20} years old.\n"
      f"In 30 years you will be {new_age30} years old.")

